The infimum of a subset S of a partially ordered set P, assuming it exists, does not necessarily belong to S. If it does, it is a minimal or least element of S. Similarly, if the supremum of S belongs to S, it is a maximal or greatest element of S

1 [2 3 4] 5 6 // no minimal element
[1 2 3 4] 5 6 // minimal element

However, the definition of maximal and minimal elements is more general. In particular, a set can have many maximal and minimal elements, whereas infima and suprema are unique.

As an example, in the collection

S = {{d, o}, {d, o, g}, {g, o, a, d}, {o, a, f}}
ordered by containment, the element {d, o} is minimal, the element {g, o, a, d} is maximal, the element {d, o, g} is neither, and the element {o, a, f} is both minimal and maximal. By contrast, neither a maximum nor a minimum exists for S.

