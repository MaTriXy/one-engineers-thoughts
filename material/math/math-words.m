An upper bound is said to be a tight upper bound, a least upper bound, or a supremum if no smaller value is an upper bound. Similarly a lower bound is said to be a tight lower bound, a greatest lower bound, or an infimum if no greater value is a lower bound.

If (L, ≤) is a partially ordered set (poset), and S⊆L is an arbitrary subset, then an element u∈L is said to be an upper bound of S if s≤u for each s∈S. A set may have many upper bounds, or none at all. An upper bound u of S is said to be its least upper bound, or join, or supremum, if u≤x for each upper bound x of S.

A partially ordered set (L, ≤) is called a join-semilattice and a meet-semilattice if each two-element subset {a,b} ⊆ L has a join (i.e. least upper bound) and a meet (i.e. greatest lower bound), denoted by a∨b and a∧b, respectively. (L, ≤) is called a lattice if it is both a join- and a meet-semilattice.

